package basicjava;

public class RelationalOperatorsDemo {

    public static void main(String[] args) {
        int a = 10, b = 5;
        System.out.println(a>b);  //true
        System.out.println(a<b);  //false
        System.out.println(a==b); //false
        System.out.println(a!=b); //true
        System.out.println(a>=b); //true
        System.out.println(a<=b); //false
    }
}
