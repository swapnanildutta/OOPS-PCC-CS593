package basicjava;

public class AssignmentOperatorsDemo {

    public static void main(String[] args) {
        int x = 5;
        x = x + 5; //x += 5
        System.out.println("x = " + x);
        x = x - 5; //x -= 5
        System.out.println("x = " + x);
        x = x * 5; //x *= 5
        System.out.println("x = " + x);
        x = x / 5; //x /= 5
        System.out.println("x = " + x);
        x = x % 5; //x %= 5
        System.out.println("x = " + x);
    }
}
